#endif

@interface HKMasterViewController () {
    NSMutableArray *_trails;
}
@end

@implementation HKMasterViewController

- (void)awakeFromNib
{
    if ([[UIDevice currentDevice] userInterfaceIdiom] == UIUserInterfaceIdiomPad) {
        self.clearsSelectionOnViewWillAppear = NO;
        self.contentSizeForViewInPopover = CGSizeMake(320.0, 600.0);
    }
    [super awakeFromNib];
}

- (void)viewDidLoad
{
    [super viewDidLoad];

    if (!_trails) {
        _trails = [[NSMutableArray alloc] init];
        
        // Get the text files containing coordinates from Resources
        NSArray *trailFiles = [[NSBundle mainBundle] URLsForResourcesWithExtension:@"txt" subdirectory:@"TrailFiles"];
        NSError *error;
        for (NSURL *url in trailFiles) {
            NSString *fileContents = [[NSString alloc] initWithContentsOfURL:url encoding:NSASCIIStringEncoding error:&error];
            HKTrail *trail = [[HKTrail alloc] initWithString:fileContents];
            [trail setTitle:[self titleFromURL:url]];
            [_trails addObject:trail];
#if HIKES_CRASH_FOR_EXPLORING_LLDB
            printf("Hikes Lite Sample App: This application has halted to explain LLDB functionality.\n\n");
            
            printf("We can add a custom formatter using Python. This will allow us to view the properties of the object inside the variables view in Xcode, any time we are paused in LLDB. And we can do this without running any code.\n\n");
            
            printf("To initialize the custom formatter, do the following:\n\n");
            printf("1.) Find the file \"CustomHikesSummaries.py\" in this project.\n");
            printf("2.) In the Xcode console type:\n\n");
            
            printf("command script import <file path to CustomHikesSummaries.py>\n\n\n");

            printf("Note that you could add this import command to ~/.lldbinit file so it will be picked up automatically each time LLDB was launched.\n\n\n");

            assert(0); // Read above instructions for initializing custom data formatters in LLDB
#endif //HIKES_CRASH_FOR_EXPLORING_LLDB
        }
    }
    

    self.detailViewController = (HKDetailViewController *)[[self.splitViewController.viewControllers lastObject] topViewController];
}

- (void)didReceiveMemoryWarning
{
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

- (BOOL)shouldAutorotateToInterfaceOrientation:(UIInterfaceOrientation)interfaceOrientation
{
    if ([[UIDevice currentDevice] userInterfaceIdiom] == UIUserInterfaceIdiomPhone) {
        return (interfaceOrientation != UIInterfaceOrientationPortraitUpsideDown);
    } else {
        return YES;
    }
}

- (void)insertNewObject:(id)sender
{
}

#pragma mark - Table View
// These methods are essentially unchanged from the Master Detail template.

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    return _trails.count;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"Cell"];

    HKTrail *trail = _trails[indexPath.row];
    cell.textLabel.text = [trail title];
    
    return cell;
}

- (BOOL)tableView:(UITableView *)tableView canEditRowAtIndexPath:(NSIndexPath *)indexPath
{
    // Return NO if you do not want the specified item to be editable.
    return NO;
}

- (void)tableView:(UITableView *)tableView commitEditingStyle:(UITableViewCellEditingStyle)editingStyle forRowAtIndexPath:(NSIndexPath *)indexPath
{
    if (editingStyle == UITableViewCellEditingStyleDelete) {
        [_trails removeObjectAtIndex:indexPath.row];
        [tableView deleteRowsAtIndexPaths:@[indexPath] withRowAnimation:UITableViewRowAnimationFade];
    } else if (editingStyle == UITableViewCellEditingStyleInsert) {
        // Create a new instance of the appropriate class, insert it into the array, and add a new row to the table view.
    }
}

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    if ([[UIDevice currentDevice] userInterfaceIdiom] == UIUserInterfaceIdiomPad) {
        HKTrail *trail = _trails[indexPath.row];
        self.detailViewController.detailItem = trail;
    }
}

- (void)prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender
{
    if ([[segue identifier] isEqualToString:@"showDetail"]) {
        NSIndexPath *indexPath = [self.tableView indexPathForSelectedRow];
        HKTrail *trail = _trails[indexPath.row];
        [[segue destinationViewController] setDetailItem:trail];
    }
}

#pragma mark Internal Methods

// A helper method to get the localized title from the filename, minus the file extension
- (NSString *)titleFromURL:(NSURL *)url
{
    NSString *title = nil;
    [url getResourceValue:&title forKey:NSURLLocalizedNameKey error:nil];
    return [title stringByDeletingPathExtension];
}

@end
