#import "APLMasterViewController.h"
#import "APLDetailViewController.h"


@interface APLMasterViewController ()
@property (nonatomic) NSMutableArray *objects;
@end


@implementation APLMasterViewController


/**
 The insertNewObjectForFetchWithCompletionHandler: method is invoked by the app delegate's application:performFetchWithCompletionHandler: method.
 This method invokes the completion handler with the argument UIBackgroundFetchResultNewData.
 */
- (void)insertNewObjectForFetchWithCompletionHandler:(void (^)(UIBackgroundFetchResult))completionHandler
{
    BLog("Update the tableview.");

    [self insertObject:[NSDate date]];
    /*
     At the end of the fetch, invoke the completion handler.
     */
    completionHandler(UIBackgroundFetchResultNewData);
}


#pragma mark - Managing objects

- (void)viewDidLoad
{
    [super viewDidLoad];
    self.navigationItem.leftBarButtonItem = self.editButtonItem;
}


- (IBAction)insertNewObject:(id)sender
{
    [self insertObject:[NSDate date]];
}


- (void)insertObject:(id)newObject
{
    [self.objects insertObject:newObject atIndex:0];
    NSIndexPath *indexPath = [NSIndexPath indexPathForRow:0 inSection:0];
    [self.tableView insertRowsAtIndexPaths:@[indexPath] withRowAnimation:UITableViewRowAnimationAutomatic];
}


-(NSMutableArray *)objects
{
    if (_objects == nil) {
        _objects = [[NSMutableArray alloc] init];
    }
    return _objects;
}


#pragma mark - Table view delegate/data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    return 1;
}


- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    return self.objects.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"Cell" forIndexPath:indexPath];

    NSDate *date = self.objects[indexPath.row];
    
    static NSDateFormatter *dateFormatter = nil;
    if (dateFormatter == nil)
    {
        dateFormatter = [[NSDateFormatter alloc] init];
        NSString *dateFormatString = [NSDateFormatter dateFormatFromTemplate:@"MMM d, hh:mm:ss a" options:0 locale:[NSLocale currentLocale]];
        [dateFormatter setDateFormat:dateFormatString];
    }
    cell.textLabel.text = [dateFormatter stringFromDate:date];

    return cell;
}


- (BOOL)tableView:(UITableView *)tableView canEditRowAtIndexPath:(NSIndexPath *)indexPath
{
    return YES;
}


- (void)tableView:(UITableView *)tableView commitEditingStyle:(UITableViewCellEditingStyle)editingStyle forRowAtIndexPath:(NSIndexPath *)indexPath
{
    if (editingStyle == UITableViewCellEditingStyleDelete)
    {
        [self.objects removeObjectAtIndex:indexPath.row];
        [tableView deleteRowsAtIndexPaths:@[indexPath] withRowAnimation:UITableViewRowAnimationFade];
    }
}


- (void)prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender
{
    if ([[segue identifier] isEqualToString:@"showDetail"])
    {
        NSIndexPath *indexPath = [self.tableView indexPathForSelectedRow];
        NSDate *object = _objects[indexPath.row];
        APLDetailViewController *destinationViewController = [segue destinationViewController];
        [destinationViewController setDetailItem:object];
    }
}


@end
