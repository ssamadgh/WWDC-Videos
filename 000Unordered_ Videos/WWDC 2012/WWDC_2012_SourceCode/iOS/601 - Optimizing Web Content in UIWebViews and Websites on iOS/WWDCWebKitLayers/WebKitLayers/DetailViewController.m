#import "DetailViewController.h"

#import "URLItem.h"

@interface DetailViewController ()
@property (strong, nonatomic) UIPopoverController *masterPopoverController;
@end

@implementation DetailViewController

- (id)initWithNibName:(NSString *)nibNameOrNil bundle:(NSBundle *)nibBundleOrNil
{
    self = [super initWithNibName:nibNameOrNil bundle:nibBundleOrNil];
    if (!self)
        return nil;
    
    self.title = @"";
    
    return self;
}

- (void)viewDidLoad
{
    [super viewDidLoad];
    
    self.webView.scalesPageToFit = YES;
    
    [self configureView];
}

- (BOOL)shouldAutorotateToInterfaceOrientation:(UIInterfaceOrientation)interfaceOrientation
{
    return (interfaceOrientation == UIInterfaceOrientationPortrait);
}


#pragma mark - Managing the detail item

- (void)setDetailItem:(URLItem *)newDetailItem
{
    if (_detailItem != newDetailItem) {
        _detailItem = newDetailItem;
        [self configureView];
    }
    
    if (self.masterPopoverController != nil)
        [self.masterPopoverController dismissPopoverAnimated:YES];
}

- (void)configureView
{
    BOOL hideButton = YES;

    if (self.detailItem) {
        self.title = self.detailItem.title;
        [self.webView loadRequest:[NSURLRequest requestWithURL:self.detailItem.url]];
        if (self.detailItem.actionTitle) {
            hideButton = NO;
            UIBarButtonItem *actionButton = [[UIBarButtonItem alloc] initWithTitle:self.detailItem.actionTitle style:UIBarButtonItemStylePlain target:self action:@selector(_barButtonAction:)];
            [self.navigationItem setRightBarButtonItem:actionButton animated:YES];
        }
    }

    if (hideButton)
        [self.navigationItem setRightBarButtonItem:nil animated:YES];
}

- (void)_barButtonAction:(id)sender
{
    if (self.detailItem && self.detailItem.handler)
        self.detailItem.handler(self.webView);
}


#pragma mark - SplitViewController Delegate

- (void)splitViewController:(UISplitViewController *)splitController willHideViewController:(UIViewController *)viewController withBarButtonItem:(UIBarButtonItem *)barButtonItem forPopoverController:(UIPopoverController *)popoverController
{
    barButtonItem.title = NSLocalizedString(@"Demo", @"Demo");
    [self.navigationItem setLeftBarButtonItem:barButtonItem animated:YES];
    self.masterPopoverController = popoverController;
}

- (void)splitViewController:(UISplitViewController *)splitController willShowViewController:(UIViewController *)viewController invalidatingBarButtonItem:(UIBarButtonItem *)barButtonItem
{
    [self.navigationItem setLeftBarButtonItem:nil animated:YES];
    self.masterPopoverController = nil;
}

@end
