# Text
The Swift standard library includes a single text type, `String`, that represents any Unicode text. You can use the `String` type–together with powerful slicing and flexible indexing operations–to consume and manipulate text safely and efficiently.

In this chapter, you will learn how to index and slice strings and customize the text representation of your types. Then you'll see how to apply these concepts to implement features in a chat app.
